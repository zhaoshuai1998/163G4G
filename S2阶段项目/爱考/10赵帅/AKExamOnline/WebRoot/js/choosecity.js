function cityName(n){
	var e = document.getElementById('city');
    for (var i=e.options.length; i>0; i--)  e.remove(i);
    if (n == 0) {
    	return;
   	}
    var a = eval("city"+ n); //得到城市的数组名
    for (var i=0; i<a.length; i++) e.options.add(new Option(a[i], a[i]));
}
